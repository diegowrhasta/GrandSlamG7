package mypackage1;

public class Modalidad 
{
  String modalidad_id;
  String nombre;

  public Modalidad()
  {
  }

  public String getModalidad_id()
  {
    return modalidad_id;
  }

  public void setModalidad_id(String newModalidad_id)
  {
    modalidad_id = newModalidad_id;
  }

  public String getNombre()
  {
    return nombre;
  }

  public void setNombre(String newNombre)
  {
    nombre = newNombre;
  }
}