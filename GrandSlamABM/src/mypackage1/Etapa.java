package mypackage1;

public class Etapa 
{
  String etapa_id;
  String nombre;

  public Etapa()
  {
  }

  public String getEtapa_id()
  {
    return etapa_id;
  }

  public void setEtapa_id(String newEtapa_id)
  {
    etapa_id = newEtapa_id;
  }

  public String getNombre()
  {
    return nombre;
  }

  public void setNombre(String newNombre)
  {
    nombre = newNombre;
  }
}